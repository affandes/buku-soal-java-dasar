package day07;

public class Latihan01 {
    public static void main(String[] args) {
        int panjang = 10;
        int lebar = 9;

        if (panjang < lebar) {
            System.out.println("Nilai panjang gak boleh lebih dari lebar!");
        }

        System.out.println("Luas = " + (panjang * lebar));
    }
}
