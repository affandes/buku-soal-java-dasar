package day07;

public class BelajarIfElse01 {
    public static void main(String[] args) {
        int nilai1 = 10;
        int nilai2 = 11;
        if (nilai1 > nilai2) {
            System.out.println("Nilai1 lebih besar dari Nilai2");
        } else {
            System.out.println("Nilai1 lebih kecil dari Nilai2");
        }
    }
}
